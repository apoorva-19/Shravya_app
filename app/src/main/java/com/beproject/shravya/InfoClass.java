package com.beproject.shravya;

public class InfoClass {
    private String shloka_id, scripture, org_shloka, parsed_shloka;
    private int chapter, verse;

    public String getShloka_id() {
        return shloka_id;
    }

    public String getScripture() {
        return scripture;
    }

    public String getOrg_shloka() {
        return org_shloka;
    }

    public String getParsed_shloka() {
        return parsed_shloka;
    }

    public int getChapter() {
        return chapter;
    }

    public int getVerse() {
        return verse;
    }
}
